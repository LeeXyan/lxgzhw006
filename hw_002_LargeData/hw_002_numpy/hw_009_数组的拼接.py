"""
数组的拼接
"""
import numpy as np

t1 = np.array([[1, 2, 3, 4],
               [2, 2, 3, 4]])
t2 = np.array([[11, 22, 33, 44],
               [33, 44, 44, 55]])

# 竖直拼接
print(np.vstack((t1, t2)))
"""
[[ 1  2  3  4]
 [ 2  2  3  4]
 [11 22 33 44]
 [33 44 44 55]]
"""
# 水平拼接
print(np.hstack((t1, t2)))
"""
[[ 1  2  3  4]
 [ 2  2  3  4]
 [11 22 33 44]
 [33 44 44 55]]
"""
